#!/bin/bash
# ==============================================================================
# 项目启动脚本 - 企业级标准
# 功能：启动本地测试节点并设置初始余额
# 使用：./start_local_node.sh
# ==============================================================================

# 停止现有的 anvil 进程
pkill -f anvil 2>/dev/null

# 等待进程完全停止
sleep 1

# 启动 anvil，分配初始余额
# --balance: 每个测试账户的初始 ETH 数量（单位: ETH，不是 wei）
# --port: 节点端口
# --host: 监听地址
anvil --balance 100000 --port 8545 --host 0.0.0.0 &

echo "✅ Anvil 本地节点已启动"
echo "   - 端口: 8545"
echo "   - 初始余额: 100000 ETH/账户"
echo "   - Chain ID: 31337"
echo ""
echo "按 Ctrl+C 停止节点"
